function OrgDashboard({ organization, dogs, dogsLoading, trialDaysRemaining, isAdmin, onDeleteDog }: {
  organization: Organization | OrgWithStats;
  dogs: DogWithPortrait[];
  dogsLoading: boolean;
  trialDaysRemaining: number;
  isAdmin: boolean;
  onDeleteDog: (dogId: number) => void;
}) {
  const [showLimitModal, setShowLimitModal] = useState(false);
  const { data: plans } = useQuery<Plan[]>({ queryKey: ["/api/plans"] });
  const currentPlan = plans?.find(p => p.id === organization.planId);
  const petLimit = (organization as any).petLimit ?? currentPlan?.dogsLimit ?? null;
  const basePetLimit = (organization as any).basePetLimit ?? currentPlan?.dogsLimit ?? null;
  const additionalPetSlots = (organization as any).additionalPetSlots ?? 0;
  const maxAdditionalSlots = (organization as any).maxAdditionalSlots ?? 5;
  const isPaidPlan = (organization as any).isPaidPlan ?? (currentPlan ? currentPlan.priceMonthly > 0 : false);
  const petCount = (organization as any).petCount ?? dogs.length;
  const atPetLimit = petLimit != null && petCount >= petLimit;
  const isCanceled = organization.subscriptionStatus === "canceled";
  const trialEndDate = organization.trialEndsAt
    ? new Date(organization.trialEndsAt)
    : organization.createdAt ? new Date(new Date(organization.createdAt).getTime() + 30 * 24 * 60 * 60 * 1000) : null;
  const isTrialExpired = organization.subscriptionStatus === "trial" && trialEndDate && trialEndDate < new Date();
  const planName = isCanceled ? "Canceled" : isTrialExpired ? "Trial Expired" : currentPlan?.name || (organization.subscriptionStatus === "trial" ? "Free Trial" : "No Plan");
  const hasPlan = !!organization.planId && organization.subscriptionStatus !== "inactive" && !isCanceled && !isTrialExpired;
  return (
    <div className="max-w-6xl mx-auto space-y-8">
      <div className="flex flex-col items-center text-center pb-6 border-b">
        <Avatar className="h-24 w-24 mb-4">
          {organization.logoUrl ? (
            <AvatarImage src={organization.logoUrl} alt={`${organization.name} logo`} data-testid="img-org-logo" />
          ) : null}
          <AvatarFallback className="bg-primary/10">
            <Heart className="h-12 w-12 text-primary" />
          </AvatarFallback>
        </Avatar>
        <h1 className="text-2xl font-serif font-bold flex items-center gap-2" data-testid="text-org-name">
          <LayoutDashboard className="h-6 w-6 text-primary" />
          {organization.name}
        </h1>
        {organization.description && (
          <p className="text-muted-foreground mt-1 max-w-lg">{organization.description}</p>
        )}
        {organization.websiteUrl && (
          <a
            href={organization.websiteUrl}
            target="_blank"
            rel="noopener noreferrer"
            className="flex items-center gap-1 text-sm text-primary hover:underline mt-2"
            data-testid="link-org-website"
          >
            <ExternalLink className="h-3 w-3" />
            Visit website
          </a>
        )}
        <Button variant="outline" size="sm" className="gap-2" data-testid="button-org-settings" asChild>
          <Link href={isAdmin ? `/admin/rescue/${organization.id}` : "/settings"} className="mt-3">
            <Settings className="h-4 w-4" />
            Rescue Settings
          </Link>
        </Button>
      </div>

      <div className="flex flex-wrap items-center justify-end gap-2">
        {hasPlan ? (
          atPetLimit ? (
            <div className="flex flex-wrap items-center gap-2">
              <span className="text-sm text-destructive font-medium" data-testid="text-pet-limit-reached">
                Pet limit reached ({petCount}/{petLimit})
              </span>
              <Button variant="outline" className="gap-2" onClick={() => setShowLimitModal(true)} data-testid="button-need-more-room">
                <Plus className="h-4 w-4" />
                Need More Room?
              </Button>
              <PetLimitModal
                open={showLimitModal}
                onOpenChange={setShowLimitModal}
                petCount={petCount}
                petLimit={petLimit}
                basePetLimit={basePetLimit ?? petLimit}
                additionalPetSlots={additionalPetSlots}
                maxAdditionalSlots={maxAdditionalSlots}
                isPaidPlan={isPaidPlan}
                hasStripeSubscription={!!(organization as any).hasActiveSubscription}
                orgId={organization.id}
                isAdmin={isAdmin}
              />
            </div>
          ) : (
            <div className="flex items-center gap-2 flex-wrap">
              <Button className="gap-2" data-testid="button-add-pet" asChild>
                <Link href={isAdmin ? `/create?org=${organization.id}` : "/create"}>
                  <Plus className="h-4 w-4" />
                  Add New Pet
                </Link>
              </Button>
              <Button variant="outline" className="gap-2" data-testid="button-import-pets" asChild>
                <Link href={isAdmin ? `/import?org=${organization.id}` : "/import"}>
                  <Download className="h-4 w-4" />
                  Import Pets
                </Link>
              </Button>
            </div>
          )
        ) : (
          <Button className="gap-2" data-testid="button-choose-plan-to-add" asChild>
            <Link href={`/choose-plan/${organization.id}`}>
              <Crown className="h-4 w-4" />
              Select a Plan to Add Pets
            </Link>
          </Button>
        )}
      </div>

      <div className="grid md:grid-cols-3 gap-4">
        <Card>
          <CardContent className="pt-6">
            <div className="flex items-center gap-3">
              <div className="p-2 rounded-lg bg-primary/10">
                <Dog className="h-5 w-5 text-primary" />
              </div>
              <div>
                <p className={`text-2xl font-bold ${atPetLimit ? "text-destructive" : ""}`} data-testid="text-dog-count">
                  {petCount}{petLimit ? ` / ${petLimit}` : ""}
                </p>
                <p className="text-sm text-muted-foreground">
                  {petLimit ? "Pets Used" : "Total Pets"}
                </p>
              </div>
            </div>
          </CardContent>
        </Card>
        <Card>
          <CardContent className="pt-6">
            <div className="flex items-center gap-3">
              <div className="p-2 rounded-lg bg-primary/10">
                <Image className="h-5 w-5 text-primary" />
              </div>
              <div>
                <p className="text-2xl font-bold" data-testid="text-credits-used">
                  {organization.portraitsUsedThisMonth || 0}{currentPlan?.monthlyPortraitCredits ? ` / ${currentPlan.monthlyPortraitCredits}` : ""}
                </p>
                <p className="text-sm text-muted-foreground">Monthly Credits Used</p>
              </div>
            </div>
          </CardContent>
        </Card>
        <Card>
          <CardContent className="pt-6">
            <div className="flex items-center gap-3">
              <div className="p-2 rounded-lg bg-primary/10">
                <Crown className="h-5 w-5 text-primary" />
              </div>
              <div className="flex-1">
                <div className="flex items-center gap-2 flex-wrap">
                  <Badge variant={organization.subscriptionStatus === "active" ? "default" : "secondary"} data-testid="badge-plan-status">
                    {planName}
                  </Badge>
                  <Button variant="ghost" size="sm" className="gap-1 h-6 text-xs" data-testid="button-change-plan" asChild>
                    <Link href={`/choose-plan/${organization.id}`}>
                      <Crown className="h-3 w-3" />
                      Change Plan
                    </Link>
                  </Button>
                </div>
                {organization.subscriptionStatus === "trial" && trialDaysRemaining > 0 && (
                  <p className="text-sm text-muted-foreground mt-1">
                    {trialDaysRemaining} days remaining
                  </p>
                )}
                {isCanceled && (
                  <p className="text-sm text-destructive mt-1">
                    Subscription canceled - select a new plan to continue
                  </p>
                )}
                {isTrialExpired && (
                  <p className="text-sm text-destructive mt-1">
                    Free trial expired - upgrade to a paid plan to continue
                  </p>
                )}
                {organization.subscriptionStatus === "inactive" && (
                  <p className="text-sm text-muted-foreground mt-1">
                    Select a plan to get started
                  </p>
                )}
              </div>
            </div>
          </CardContent>
        </Card>
      </div>

      <div>
        <div className="flex items-center justify-between mb-4 gap-2 flex-wrap">
          <h2 className="text-xl font-semibold" data-testid="text-adoptable-pets">Adoptable Pets</h2>
          <div className="flex items-center gap-2">
            {organization.slug && (
              <Button variant="ghost" size="sm" className="gap-1" data-testid="button-view-showcase" asChild>
                <Link href={`/rescue/${organization.slug}`}>
                  <Eye className="h-3.5 w-3.5" />
                  Rescue Showcase
                </Link>
              </Button>
            )}
            <Button variant="ghost" size="sm" className="gap-1" asChild>
              <Link href="/gallery">
                View Public Gallery
                <ExternalLink className="h-3 w-3" />
              </Link>
            </Button>
          </div>
        </div>

        {dogsLoading ? (
          <div className="grid sm:grid-cols-2 lg:grid-cols-3 gap-4">
            {[1, 2, 3].map((i) => (
              <Skeleton key={i} className="h-64" />
            ))}
          </div>
        ) : dogs.length === 0 ? (
          <Card className="py-12">
            <CardContent className="text-center">
              <Heart className="h-16 w-16 mx-auto mb-4 text-muted-foreground/50" />
              <h3 className="text-lg font-medium mb-2">No pets yet</h3>
              <p className="text-muted-foreground mb-4">
                {hasPlan
                  ? "Add your first rescue pet to create a beautiful portrait"
                  : "Select a plan to start adding your rescue pets"}
              </p>
              {hasPlan ? (
                <div className="flex items-center justify-center gap-3 flex-wrap">
                  <Button className="gap-2" asChild>
                    <Link href={isAdmin ? `/create?org=${organization.id}` : "/create"}>
                      <Plus className="h-4 w-4" />
                      Add Your First Pet
                    </Link>
                  </Button>
                  <Button variant="outline" className="gap-2" asChild>
                    <Link href={isAdmin ? `/import?org=${organization.id}` : "/import"}>
                      <Download className="h-4 w-4" />
                      Import Pets
                    </Link>
                  </Button>
                </div>
              ) : (
                <Button className="gap-2" asChild>
                  <Link href={`/choose-plan/${organization.id}`}>
                    <Crown className="h-4 w-4" />
                    Choose a Plan
                  </Link>
                </Button>
              )}
            </CardContent>
          </Card>
        ) : (
          <div className="grid sm:grid-cols-2 lg:grid-cols-3 gap-4">
            {dogs.map((dog) => (
              <Card key={dog.id} className="overflow-hidden group" data-testid={`card-dog-${dog.id}`}>
                <Link href={`/pawfile/${dog.id}`}>
                  <div className="aspect-square relative bg-muted protected-image-wrapper">
                    {dog.portrait?.generatedImageUrl ? (
                      <img
                        src={dog.portrait.generatedImageUrl}
                        alt={`${dog.name} portrait`}
                        className="w-full h-full object-cover protected-image"
                        draggable={false}
                      />
                    ) : dog.originalPhotoUrl ? (
                      <img
                        src={dog.originalPhotoUrl}
                        alt={dog.name}
                        className="w-full h-full object-cover protected-image"
                        draggable={false}
                      />
                    ) : (
                      <div className="w-full h-full flex items-center justify-center">
                        {dog.species === "cat" ? (
                          <Cat className="h-16 w-16 text-muted-foreground/30" />
                        ) : (
                          <Dog className="h-16 w-16 text-muted-foreground/30" />
                        )}
                      </div>
                    )}
                    <div className="absolute top-0 left-0 right-0 p-3 bg-gradient-to-b from-black/70 via-black/40 to-transparent">
                      <h3 className="font-serif font-bold text-lg text-white drop-shadow-lg" data-testid={`text-dog-name-${dog.id}`}>
                        {dog.name}
                      </h3>
                    </div>
                    {!dog.portrait?.generatedImageUrl && (
                      <div className="absolute inset-0 bg-black/50 flex items-center justify-center invisible group-hover:visible transition-all">
                        <span className="text-white text-sm font-medium">View Profile</span>
                      </div>
                    )}
                  </div>
                </Link>
                <CardFooter className="p-3 flex flex-col gap-2">
                  <div className="flex items-center gap-2 w-full">
                    <div className="flex-1 min-w-0">
                      <p className="text-sm text-muted-foreground truncate">
                        {dog.breed} {dog.age ? `\u2022 ${dog.age}` : ""}
                      </p>
                    </div>
                    {dog.isAvailable && (
                      <Badge variant="outline" className="shrink-0">Available</Badge>
                    )}
                    {!dog.adoptionUrl && (
                      <Badge variant="outline" className="shrink-0 border-amber-500 text-amber-600 bg-amber-50 dark:bg-amber-950/30">Needs URL</Badge>
                    )}
                    {dog.adoptionUrl && (
                      <a
                        href={dog.adoptionUrl}
                        target="_blank"
                        rel="noopener noreferrer"
                        onClick={(e) => e.stopPropagation()}
                        data-testid={`link-adopt-${dog.id}`}
                      >
                        <Button variant="outline" size="icon" title="View adoption page">
                          <ExternalLink className="h-4 w-4" />
                        </Button>
                      </a>
                    )}
                    <Button
                      variant="ghost"
                      size="icon"
                      className="text-destructive shrink-0"
                      onClick={(e) => {
                        e.stopPropagation();
                        onDeleteDog(dog.id);
                      }}
                      data-testid={`button-delete-dog-${dog.id}`}
                      title="Remove pet"
                    >
                      <Trash2 className="h-4 w-4" />
                    </Button>
                  </div>
                  <Button variant="outline" size="sm" className="w-full gap-2" data-testid={`button-new-portrait-${dog.id}`} asChild>
                    <Link
                      href={isAdmin ? `/create?dog=${dog.id}&org=${organization.id}` : `/create?dog=${dog.id}`}
                      className="w-full"
                      onClick={(e: React.MouseEvent) => e.stopPropagation()}
                    >
                      <Sparkles className="h-4 w-4" />
                      {dog.portrait?.generatedImageUrl ? "New Portrait" : `Create ${dog.name}'s Portrait`}
                    </Link>
                  </Button>
                </CardFooter>
              </Card>
            ))}
          </div>
        )}
      </div>
    </div>
  );
}
